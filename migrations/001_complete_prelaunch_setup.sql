-- Function: Validate and apply coupon
CREATE OR REPLACE FUNCTION validate_and_apply_coupon(
    p_coupon_code text,
    p_user_id uuid,
    p_order_total numeric,
    p_menu_item_ids bigint[]
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
    v_coupon record;
    v_usage_count int;
    v_discount numeric;
BEGIN
    SELECT * INTO v_coupon
    FROM coupons
    WHERE code = UPPER(p_coupon_code)
    AND is_active = true
    AND (expires_at IS NULL OR expires_at > now());
    
    IF NOT FOUND THEN
        RETURN jsonb_build_object('valid', false, 'message', 'ไม่พบคูปองนี้');
    END IF;
    
    IF p_order_total < v_coupon.min_order_amount THEN
        RETURN jsonb_build_object('valid', false, 'message', 'ยอดสั่งซื้อขั้นต่ำ ' || v_coupon.min_order_amount || ' บาท');
    END IF;
    
    IF v_coupon.usage_limit IS NOT NULL AND v_coupon.usage_count >= v_coupon.usage_limit THEN
        RETURN jsonb_build_object('valid', false, 'message', 'คูปองหมดแล้ว');
    END IF;
    
    SELECT COUNT(*) INTO v_usage_count
    FROM coupon_usages
    WHERE coupon_id = v_coupon.id AND user_id = p_user_id;
    
    IF v_usage_count >= v_coupon.per_user_limit THEN
        RETURN jsonb_build_object('valid', false, 'message', 'คุณใช้คูปองนี้ครบกำหนดแล้ว');
    END IF;
    
    IF v_coupon.discount_type = 'fixed' THEN
        v_discount := v_coupon.discount_value;
    ELSIF v_coupon.discount_type = 'percentage' THEN
        v_discount := (p_order_total * v_coupon.discount_value / 100);
        IF v_coupon.max_discount IS NOT NULL THEN
            v_discount := LEAST(v_discount, v_coupon.max_discount);
        END IF;
    ELSE
        v_discount := 0;
    END IF;
    
    RETURN jsonb_build_object(
        'valid', true,
        'coupon_id', v_coupon.id,
        'discount', v_discount,
        'name', v_coupon.name
    );
END;
$$;

-- Function: Check if shop is open
CREATE OR REPLACE FUNCTION is_shop_open()
RETURNS boolean
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
    v_config jsonb;
    v_now time;
    v_today int;
BEGIN
    SELECT value INTO v_config FROM shop_config WHERE key = 'shop_hours';
    v_now := current_time;
    v_today := EXTRACT(DOW FROM current_date);
    
    IF NOT (v_today = ANY(ARRAY(SELECT jsonb_array_elements_text(v_config->'days_open')::int))) THEN
        RETURN false;
    END IF;
    
    IF v_now < (v_config->>'open')::time OR v_now > (v_config->>'close')::time THEN
        RETURN false;
    END IF;
    
    RETURN true;
END;
$$;

-- Function: Get available slots
CREATE OR REPLACE FUNCTION get_available_slots(
    p_date date,
    p_delivery_method text
)
RETURNS TABLE (
    slot_time time,
    available int,
    max_orders int
)
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
    PERFORM generate_order_slots(p_date, p_delivery_method);
    
    RETURN QUERY
    SELECT 
        s.slot_time,
        (s.max_orders - s.current_orders) as available,
        s.max_orders
    FROM order_slots s
    WHERE s.slot_date = p_date
    AND s.delivery_method = p_delivery_method
    AND s.is_locked = false
    AND s.slot_time > current_time
    ORDER BY s.slot_time;
END;
$$;

