-- Pre-generate slots (function)
CREATE OR REPLACE FUNCTION generate_order_slots(
    p_date date,
    p_delivery_method text
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
    v_slot_time time;
    v_open_time time;
    v_close_time time;
    v_slot_duration interval := '30 minutes';
BEGIN
    SELECT (value->>'open')::time, (value->>'close')::time
    INTO v_open_time, v_close_time
    FROM shop_config WHERE key = 'shop_hours';
    
    v_slot_time := v_open_time;
    
    WHILE v_slot_time < v_close_time LOOP
        INSERT INTO order_slots (slot_date, slot_time, delivery_method, max_orders)
        VALUES (p_date, v_slot_time, p_delivery_method, 20)
        ON CONFLICT (slot_date, slot_time, delivery_method) DO NOTHING;
        
        v_slot_time := v_slot_time + v_slot_duration;
    END LOOP;
END;
$$;

