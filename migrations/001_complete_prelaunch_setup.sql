-- Function: Auto-update inventory when order is placed
CREATE OR REPLACE FUNCTION deduct_inventory_on_order()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
    v_item record;
    v_ingredient record;
    v_total_used numeric;
BEGIN
    IF NEW.status IN ('confirmed', 'preparing') AND OLD.status = 'placed' THEN
        FOR v_item IN SELECT * FROM jsonb_to_recordset(NEW.items) AS x(menu_item_id bigint, quantity int)
        LOOP
            FOR v_ingredient IN 
                SELECT i.*, mi.quantity_used 
                FROM menu_item_ingredients mi
                JOIN inventory_items i ON mi.inventory_id = i.id
                WHERE mi.menu_item_id = v_item.menu_item_id
            LOOP
                v_total_used := v_ingredient.quantity_used * v_item.quantity;
                
                UPDATE inventory_items 
                SET current_stock = current_stock - v_total_used,
                    updated_at = now()
                WHERE id = v_ingredient.id;
                
                INSERT INTO inventory_transactions (inventory_id, type, quantity, reason, order_id)
                VALUES (v_ingredient.id, 'out', v_total_used, 'Order #' || NEW.id, NEW.id);
            END LOOP;
        END LOOP;
    END IF;
    
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tr_deduct_inventory ON public.orders;
CREATE TRIGGER tr_deduct_inventory
    AFTER UPDATE ON public.orders
    FOR EACH ROW
    EXECUTE FUNCTION deduct_inventory_on_order();

-- Function: Check and auto-disable menu when inventory is low
CREATE OR REPLACE FUNCTION check_low_inventory()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
    IF NEW.current_stock <= NEW.min_stock THEN
        UPDATE menu_items 
        SET is_available = false
        WHERE id IN (
            SELECT menu_item_id 
            FROM menu_item_ingredients 
            WHERE inventory_id = NEW.id AND is_required = true
        );
    END IF;
    
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tr_check_low_inventory ON public.inventory_items;
CREATE TRIGGER tr_check_low_inventory
    AFTER UPDATE ON public.inventory_items
    FOR EACH ROW
    WHEN (OLD.current_stock > NEW.min_stock AND NEW.current_stock <= NEW.min_stock)
    EXECUTE FUNCTION check_low_inventory();

