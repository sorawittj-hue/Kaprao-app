-- SECTION 10 : TRIGGER — auto-create profile on signup
-- รองรับทั้ง LINE login + Guest (Anonymous)
-- ============================================================

CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
    INSERT INTO public.profiles (id, display_name, points, total_orders, tier, updated_at)
    VALUES (
        NEW.id,
        COALESCE(NEW.raw_user_meta_data->>'full_name', NEW.raw_user_meta_data->>'name', 'ลูกค้า'),
        0,
        0,
        'MEMBER',
        now()
    )
    ON CONFLICT (id) DO NOTHING;
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
AFTER INSERT ON auth.users
FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();


-- ============================================================
-- SECTION 11 : TRIGGER — handle_order_placed
-- เมื่อมีออเดอร์ใหม่ → อัพเดตแต้ม + นับออเดอร์ + สร้างตั๋วหวย
-- ============================================================

CREATE OR REPLACE FUNCTION public.handle_order_placed()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
    earned_points   int := 0;
    lotto_num       text;
    next_draw       date;
    current_points  int;
    day_now         int;
    month_now       int;
    year_now        int;
BEGIN
    -- รันเฉพาะเมื่อ status = 'placed' (INSERT ใหม่ หรือ UPDATE มาจาก status อื่น)
    IF NEW.status = 'placed' AND (TG_OP = 'INSERT' OR OLD.status != 'placed') THEN

        -- ─── 1. คำนวณแต้มที่ได้รับ ─────────────────────────────
        IF NEW.points_earned IS NOT NULL AND NEW.points_earned > 0 THEN
            earned_points := NEW.points_earned;
        ELSE
            -- 1 แต้มต่อ 10 บาท
            earned_points := FLOOR(COALESCE(NEW.total_price, 0) / 10);
        END IF;

        -- ─── 2. อัพเดต profiles (เฉพาะ user ที่ล็อกอิน) ────────
        IF NEW.user_id IS NOT NULL THEN

            -- 2a. หักแต้มที่ใช้แลก (ถ้ามี)
            IF COALESCE(NEW.points_redeemed, 0) > 0 THEN
                UPDATE public.profiles
                SET points      = GREATEST(0, points - NEW.points_redeemed),
                    updated_at  = now()
                WHERE id = NEW.user_id;

                SELECT points INTO current_points
                FROM public.profiles WHERE id = NEW.user_id;

                INSERT INTO public.point_logs
                    (user_id, action, amount, order_id, note, balance_after)
                VALUES
                    (NEW.user_id, 'REDEEM', -NEW.points_redeemed, NEW.id,
                     'แลกส่วนลด Order #' || NEW.id, COALESCE(current_points, 0));
            END IF;

            -- 2b. เพิ่มแต้มที่ได้ + นับออเดอร์
            UPDATE public.profiles
            SET points          = points + earned_points,
                total_orders    = total_orders + 1,
                -- อัพเดต tier อัตโนมัติ
                tier = CASE
                    WHEN (points + earned_points) >= 2000 THEN 'VIP'
                    WHEN (points + earned_points) >= 1000 THEN 'GOLD'
                    WHEN (points + earned_points) >= 500  THEN 'SILVER'
                    ELSE 'MEMBER'
                END,
                updated_at = now()
            WHERE id = NEW.user_id;

            IF earned_points > 0 THEN
                SELECT points INTO current_points
                FROM public.profiles WHERE id = NEW.user_id;

                INSERT INTO public.point_logs
                    (user_id, action, amount, order_id, note, balance_after)
                VALUES
                    (NEW.user_id, 'EARN', earned_points, NEW.id,
                     'สั่งอาหาร Order #' || NEW.id, COALESCE(current_points, 0));
            END IF;

            -- ─── 3. สร้างตั๋วหวย ──────────────────────────────────
            lotto_num := LPAD(RIGHT(NEW.id::text, 2), 2, '0');

            day_now   := EXTRACT(DAY   FROM now())::int;
            month_now := EXTRACT(MONTH FROM now())::int;
            year_now  := EXTRACT(YEAR  FROM now())::int;

            IF    day_now >= 1  AND day_now < 16 THEN
                next_draw := make_date(year_now, month_now, 16);
            ELSIF day_now >= 16 THEN
                -- วันที่ 1 ของเดือนถัดไป
                next_draw := (date_trunc('month', now()) + interval '1 month')::date;
            ELSE
                next_draw := make_date(year_now, month_now, 1);
            END IF;

            INSERT INTO public.lotto_pool (order_id, user_id, number, draw_date)
            VALUES (NEW.id, NEW.user_id, lotto_num, next_draw)
            ON CONFLICT (order_id) DO NOTHING;

        END IF; -- end user_id IS NOT NULL
    END IF; -- end status = 'placed'

    RETURN NEW;
END;
$$;

