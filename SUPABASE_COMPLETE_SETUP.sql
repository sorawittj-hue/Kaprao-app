-- ใช้สำหรับ LINE login flow — หา profile จาก line_user_id
-- ถ้ายังไม่มี → สร้าง (UPSERT-safe)
-- ============================================================

-- ลบทุก signature เก่าของ function นี้
DROP FUNCTION IF EXISTS public.get_or_create_profile_by_line_id(uuid, text, text);
DROP FUNCTION IF EXISTS public.get_or_create_profile_by_line_id(uuid, text, text, text);

CREATE OR REPLACE FUNCTION public.get_or_create_profile_by_line_id(
    p_supabase_uid  uuid,
    p_line_user_id  text,
    p_display_name  text,
    p_picture_url   text DEFAULT NULL
)
RETURNS public.profiles
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
    result public.profiles;
BEGIN
    -- Upsert: สร้างถ้าไม่มี, อัพเดตถ้ามีแล้ว
    INSERT INTO public.profiles
        (id, line_user_id, display_name, picture_url, points, total_orders, tier, updated_at)
    VALUES
        (p_supabase_uid, p_line_user_id, p_display_name, p_picture_url, 0, 0, 'MEMBER', now())
    ON CONFLICT (id) DO UPDATE
        SET line_user_id  = EXCLUDED.line_user_id,
            display_name  = COALESCE(EXCLUDED.display_name, profiles.display_name),
            picture_url   = COALESCE(EXCLUDED.picture_url,  profiles.picture_url),
            updated_at    = now()
    RETURNING * INTO result;

    RETURN result;
END;
$$;

-- ระบุ argument list ให้ชัดเจน
GRANT EXECUTE ON FUNCTION public.get_or_create_profile_by_line_id(uuid, text, text, text) TO authenticated;
GRANT EXECUTE ON FUNCTION public.get_or_create_profile_by_line_id(uuid, text, text, text) TO anon;


