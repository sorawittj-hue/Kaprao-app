CREATE OR REPLACE FUNCTION public.update_user_points(
    p_user_id   uuid,
    p_amount    int,
    p_action    text DEFAULT 'ADJUST',
    p_note      text DEFAULT NULL,
    p_order_id  bigint DEFAULT NULL
)
RETURNS int
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
    new_balance int;
BEGIN
    -- อัพเดตแต้ม (ป้องกันไม่ให้ติดลบ)
    UPDATE public.profiles
    SET points      = GREATEST(0, points + p_amount),
        tier = CASE
            WHEN GREATEST(0, points + p_amount) >= 2000 THEN 'VIP'
            WHEN GREATEST(0, points + p_amount) >= 1000 THEN 'GOLD'
            WHEN GREATEST(0, points + p_amount) >= 500  THEN 'SILVER'
            ELSE 'MEMBER'
        END,
        updated_at  = now()
    WHERE id = p_user_id
    RETURNING points INTO new_balance;

    -- บันทึก log
    INSERT INTO public.point_logs (user_id, action, amount, order_id, note, balance_after)
    VALUES (p_user_id, p_action, p_amount, p_order_id,
            COALESCE(p_note, p_action || ' ' || p_amount || ' points'),
            COALESCE(new_balance, 0));

    RETURN COALESCE(new_balance, 0);
END;
$$;

-- ระบุ argument list ให้ชัดเจน เพื่อหลีกปัญหา ambiguous
GRANT EXECUTE ON FUNCTION public.update_user_points(uuid, int, text, text, bigint) TO authenticated;
GRANT EXECUTE ON FUNCTION public.update_user_points(uuid, int, text, text, bigint) TO anon;


